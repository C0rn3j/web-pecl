<h1>PEAR Meeting in Amsterdam</h1>

<div style="margin-left:2em;margin-right:2em">
We will hold the first PEAR Meeting in Amsterdam on May 9th at 17:30.
<br /><br />
The <a href="http://www.terena.nl/about/secretariat/location.html">location</a>
has been graciously sponsored by Jeroen Houben. We will provide a live
stream from the meeting so that people that did not make it to the meeting in
person can participate via web. Apart from that there will be the
possibility to join the discussion via IRC.
<br /><br />
The current preliminary agenda is as follows:
<ul>
<li>Quality vs. Quantity?</li>
<li>Possible regulations to prevent some of the recent conflicts on the mailinglist</li>
<li>PEAR CS (method naming conventions?)</li>
<li><a href="http://marc.theaimsgroup.com/?l=pear-dev&m=104617534710384&w=2">PFC RfC</a></li>
<li>PEAR Installer</li>
<li>PEAR on windows</li>
<li>PEARweb (rating system, comment system, package proposal)</li>
<li>PEAR promotion</li>
<li>PHP 5</li>
</ul>

The following people will participate in person:<br />
Sebastian Bergmann, Hartmut Holzgraefe, Jeroen Houben, 
Pierre-Alain Joye, Alan Knowles, Wolfram Kriesing, Jan Lehnardt,
Derick Rethans, George Schlossnagle, Lukas Smith, Markus Wolff.
<br /><br />

Everyone who will participate should be prepared accordingly. The
meeting will probably last about 4 hours with a short break. Jeroen can
probably provide drinks but if we want food we will have to
come up with something ourselfs (like ordering pizza). All the people
listed above will be contacted by me.
<br /><br />
Please note that while we want this meeting to be as open as possible,
we may have to limit participation if the interest becomes too great. The
meeting room can hold up to 15 people. We may also have to moderate the IRC
channel if things become too confusing to follow. When necessary newer
members of the PEAR community may therefore be unable to directly
communicate with the meeting. I would therefore suggest to newer
members to write down their ideas as a well thought out RfC and post
it to the <a href="mailto:pear-dev@lists.php.net">mailinglist</a>
or send it to <a href="mailto:smith@backendmedia.com">me</a> directly.
That way we can more easily incorporate their ideas into the meeting.
<br /><br />
Please mail all comments to either <a href="mailto:pear-dev@lists.php.net">pear-dev@lists.php.net</a>
or <a href="mailto:smith@backendmedia.com">Lukas Smith</a>.
<br /><br />
On behalf of the PEAR developers: Lukas Smith.
